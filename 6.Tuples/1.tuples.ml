(* Program to declare tuple and use it in function *)

(* Declaring the tuple with first element as integer and second as string/. *)
(* In Ocaml, we have to specify the data type of each element by using * operator in between *)

(* Example 1 :  *)
let tuple1 : int * string  = (10 , "Hello");;
(* Printing the value of variable 1 tuple1 *)
tuple1

(* Example 2 :  *)
let tuple2 : string * bool  = ("Akshay" , true);;
(* Printing the value of variable 2 tuple2 *)
tuple2

