(* Program to find Area Of Trapezoid *)
(* ============================================================ *)

(* Declaring Variable for storing value of base 1 of Trapezoid*)
let b1 : int = 5
(* Declaring Variable for storing value of base 2 of Trapezoid*)
let b2 : int = 5
(* Declaring Variable for storing value of height of Trapezoid *)
let h : int = 4
(* Declaring Variable for claculating and storing value of area *)
let area : int = (b1 + b2) * h;;

(* Printing the value of the calculated Area *)
area;;